                <section id="ResponsabilidadeCivil">
                    <h2 class="Azul">
                        15 - Responsabilidade Civil do Estado
                    </h2>
                    <hr class="hr4">

                    <p class="Texto">
                        <strong class="Azul">Objetivo:</strong> Exibir os aspectos gerais a respeito da responsabilidade civil do Estado.
                    </p>
                    <hr class="hrLaranja">

                    <ul>
                        <li style="color: #286ce6">
                            <h4><strong><u>Fundamento da Responsabilidade:</u></strong></h4>
                        </li>
                    </ul>

                    <p class="Texto">
                        O que é responsabilidade?<br><br>
                        I. Responsabilização do ente público pelos danos que seus agentes causam a particulares, nesta qualidade.<br>
                        II. Responsabilidade extracontratual:<br>
                        - Não decorre de qualquer contrato ou vínculo anterior com o sujeito indenizado.
                        III Fundamento principiológico:<br>
                        - Princípio da indisponibilidade do interesse público – limites a atuação.
                        - Princípio da Isonomia (igualdade): Quando o Estado causa um dano específico a alguém ou a pequeno grupo de pessoas, nada mais justo que os sujeitos prejudicados sejam indenizados, como forma de reparar a desigualdade causada pela atuação estatal.
                        </p>
                    <hr>

                    <ul>
                        <li style="color: #286ce6">
                            <h4><strong><u>Responsabilidade Civil do Estado na ordem jurídica da CF/1988</u></strong></h4>
                        </li>
                    </ul>

                    <p class="Texto">
                        <strong><u>Art. 37, §6º, Constituição Federal:</u></strong>
                        </p>

                    <p class="Texto">
                        “As pessoas jurídicas de direito público e as de direito privado prestadoras de serviços públicos responderão pelos danos que seus agentes, nessa qualidade, causarem a terceiros, assegurado o direito de regresso contra o responsável nos casos de dolo ou culpa.”
                        </p>

                    <p class="Texto">
                        <strong><u>Art. 43, Código Civil:</u></strong>
                    </p>

                    <p class="Texto">
                        “As pessoas jurídicas de direito público interno são civilmente responsáveis por atos dos seus agentes que nessa qualidade causem danos a terceiros, ressalvado direito regressivo contra os causadores do dano, se houver, por parte destes, culpa ou dolo.”
                        </p>

                    <p class="Texto">
                        <strong><u>Agentes da Responsabilidade Civil:</u></strong>
                        </p>

                    <p class="Texto">
                        - A responsabilidade do Estado é objetiva, mas a responsabilização do agente, perante o Estado, é subjetiva, decorrente da comprovação de dolo ou de culpa.<br>
                        - Incluem-se nessa teoria: Todas as pessoas jurídicas de Direito Público, autarquias, fundações públicas. Fazem parte, ainda, as empresas públicas e sociedades de economia mista voltadas a prestação de serviços públicos. Ou seja, não abrangem as empresas estatais que apenas exploram a atividade econômica.<br>
                        Observação: Incluem-se, ainda: Particulares prestadores de serviço público por delegação (concessionárias e permissionárias). A responsabilidade é objetiva e o Estado tem responsabilidade subsidiária (exige o esgotamento das tentativas de pagamento por parte da empresa) – e objetiva – pela atuação do particular.<br>
                        Exemplo: ônibus – transporte público.<br><br>
                        - Para incidir essa teoria é necessário comprovar alguns elementos:<br>
                        1) A conduta do Estado;<br>
                        2) O dano;<br>
                        3) O nexo de causalidade; <br>
                        4) O elemento subjetivo, qual seja, a culpa ou o dolo do agente (culpa em sentido amplo).<br><br>
                        Observação: Diferença entre responsabilidade subsidiária e responsabilidade solidária: No primeiro caso, a obrigação de reparar o dano é da pessoa jurídica prestadora do serviço e, caso seja inviável esse pagamento, o Estado é chamado à responsabilidade. No segundo caso, ambas respondem, ao mesmo tempo.
                        </p>
                    <hr>

                    <ul>
                        <li style="color: #286ce6">
                            <h4><strong><u>Teorias da responsabilidade civil objetiva do Estado</u></strong></h4>
                        </li>
                    </ul>

                    <p class="Texto">
                        <strong><u>Teoria do risco administrativo:</u></strong>
                    </div>

                    <p class="Texto">
                        - O Estado é realmente um sujeito político, jurídico e economicamente mais poderoso que o administrado, gozando de determinadas prerrogativas não estendidas aos demais sujeitos de direito.<br>
                        - Em razão disso, passou-se a considerar que por ser mais poderoso, o Estado teria que arcar com um 32
                        risco maior decorrente de suas atividades.
                        </p>

                    <p class="Texto">
                        <strong><u>Teoria do risco integral:</u></strong>
                        </p>

                    <p class="Texto">
                        - Parte da premissa de que o ente público é garantidor universal e, sendo assim, a simples existência de dano e do nexo causal é suficiente para que surja a obrigação de indenizar para a Administração, não admitindo excludentes de responsabilidade.<br><br>
                        - Responsabilização absoluta do Estado.<br><br>
                        - É adotada somente em situações excepcionais:<br>
                        I. Dano decorrente de atividade nuclear exercida pelo Estado ou autorizada pelo mesmo.
                        II. Dano ao meio ambiente, quanto aos atos comissivos.
                        III. Crimes ocorridos a bordo de aeronaves decorrentes de ataques terroristas.
                        </p>
                    <hr>

                    <ul>
                        <li style="color: #286ce6">
                            <h4><strong><u>Responsabilidade por Omissão do Estado</u></strong></h4>
                        </li>
                    </ul>

                    <p class="Texto">
                        <strong><u>Teoria do risco integral:</u></strong>
                        </p>

                    <p class="Texto">
                        - Situações em que o dano é causado a um particular em virtude de uma não atuação do agente público;<br>
                        - É o “não agir, não fazer” do Estado;<br>
                        - Aplica-se a teoria da responsabilidade subjetiva, ou seja, <u>precisa comprovar a conduta, o nexo causal, o dano e a culpa em sentido amplo.</u>
                        </p>
                    <hr>

                    <ul>
                        <li style="color: #286ce6">
                            <h4><strong><u>Excludentes da Responsabilidade Civil Objetiva</u></strong></h4>
                        </li>
                    </ul>

                    <p class="Texto">
                        - A ausência de qualquer dos requisitos (conduta do agente público, dano e nexo de causalidade).<br><br>
                        - Caso Fortuito: ‘Situação imprevisível”<br><br>
                        - Força Maior: “Situação insuperável”.<br><br>
                        - Culpa exclusiva da vítima.<br><br>
                        - Culpa concorrente: Tanto o Estado quanto a vítima concorreram para causar o dano.
                        </p>
                    <hr>

                    <ul>
                        <li style="color: #286ce6">
                            <h4><strong><u>Prazo prescricional</u></strong></h4>
                        </li>
                    </ul>

                    <p class="Texto">
                        - Prazo de: 5 (cinco) anos – Art. 1º decreto 20.910/31 e art. 1 º-C da Lei 9.494/97;<br><br>
                        - Código Civil fala em 3 (três) anos – Art. 206, §3º; <br><br>
                        - STJ e STF entendem aplicáveis o prazo de 5 (cinco) anos.<br><br>
                        <u>Exceção:</u> Caso o dano tenha sido causado por um particular, a ação de ressarcimento em face desse sujeito prescreve em conformidade com a legislação civil, ou seja, 3 anos.<br><br>
                        <u>Exceção 2:</u> É imprescritível a pretensão de recebimento de indenização por danos morais decorrente da violação aos direitos humanos durante o regime militar de exceção (STJ RE 816.209 RJ).
                        </p>



