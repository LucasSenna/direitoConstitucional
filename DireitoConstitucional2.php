                        - Norma de Eficácia limitada, aplicabilidade através da promulgação da Lei nº 8.429/92.<br>
                        - Ampliação de sanções mínimas já previstas na CF/88 não é inconstitucional (AgRg no RE 598.588).
                        </p>
