                    <hr class="hrLaranja">

                    <p class="Texto">
                        Os agentes públicos, no decorrer do exercício de suas atribuições, podem vir a praticar condutas que violem o Direito, condutas essas suscetíveis a diversas formas de punição. As responsabilizações podem ocorrer em três esferas, a civil, penal e administrativa, onde são corriqueiramente chamadas de tríplice responsabilidade do agente público.<br><br>
                        Após a repercussão civil, penal e administrativa, ainda há uma quarta esfera de responsabilização, a que decorre da aplicação da Lei de Improbidade Administrativa (LIA)- Lei nº 8.429/92. A doutrina atual ainda estabelece mais duas esferas de responsabilização:<br>
                        - A política pela prática de crimes de responsabilidade (Lei nº 1.079/50);<br>
                        - O processo de controle.
