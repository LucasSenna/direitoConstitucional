                        Ex.: Se um ente da Administração Indireta comete ato ilícito, uma ação é movida contra ele, e não contra o ente da Adm. Direta.
                        </p>

                    <p class="Texto">
                        <strong><u>Criação e extinção mediante lei específica (lei ordinária):</u></strong>
                        </p>

                    <p class="Texto">
                        - A lei específica CRIA somente as autarquias, de modo que basta a aprovação da lei para a autarquia começar a existir.<br>
                        - A lei específica AUTORIZA A CRIAÇÃO dos demais entes, quais sejam fundações públicas, empresas públicas e sociedades de economia mista. Assim, a entidade somente passará a existir juridicamente com o registro do seu ato constitutivo no órgão competente – cartório de PJ ou Junta comercial).
                        </p>

                    <div class="container">
                        <div class="row">
                            <div class="col-sm">
                            </div>
                            <div class="col-8">
                                <div class="jumbotron ">
                                    <div class="container">
                                        <p>
                                            “Art. 37 - XIX - somente por lei específica poderá ser criada autarquia e autorizada a instituição de empresa pública, de sociedade de economia mista e de fundação, cabendo à lei complementar, neste último caso, definir as áreas de sua atuação;”
                                        </p>
                                    </div>
                                </div>
                            </div>
                            <div class="col-sm"></div>
                        </div>
                    </div>

                    <p class="Texto">
                        <strong><u>Finalidade pública:</u></strong>
                        </p>

                    <p class="Texto">
                        - O ente já é criado com uma finalidade pública específica, de modo que não pode exercer atribuições livremente. - Prestação de serviços públicos.<br>
                        - Exploração de atividade econômica. Lucro: Finalidade ou consequência?
                        </p>

                    <div class="container">
                        <div class="row">
                            <div class="col-sm">
                            </div>
                            <div class="col-8">
                                <div class="jumbotron ">
                                    <div class="container">
                                        <p>
                                            “Art. 173. Ressalvados os casos previstos nesta Constituição, a exploração direta de atividade econômica pelo Estado só será permitida quando necessária aos imperativos da segurança nacional ou a relevante interesse coletivo, conforme definidos em lei.”
                                        </p>
                                    </div>
                                </div>
                            </div>
                            <div class="col-sm"></div>
                        </div>
                    </div>

                    <p class="Texto">
                        <strong><u>Controle finalístico – Tutela – “Supervisão ministerial” – Vinculação administrativa:</u></strong>
                        </p>

                    <p class="Texto">
                        - Os entes da Administração Indireta se sujeitam ao <strong>Controle</strong> pela Administração direta da pessoa política á qual são vinculados;<br>
                        - Controle de Legalidade;<br>
                        - A doutrina de Fernanda Marinela preceitua que este controle visa “assegurara o cumprimento dos objetivos fixados em seus atos de criação; harmonizar sua atuação com a política e programação do Governo; zelar pela obtenção de eficiência administrativa, operacional e financeira”.<br><br>
                        Observação: Não há subordinação ou hierarquia entre os entes.<br><br>
                        Exemplo: O INSS é uma autarquia federal vinculado ao Ministério da Previdência. De uma decisão do INSS, pode caber <strong>“recurso hierárquico impróprio”</strong> dirigido ao Ministério em caso de <strong>ilegalidade ou abuso de poder, desde que haja previsão legal expressa.</strong>
                        </p>

                    <p class="Texto">
                        <strong><u>Sujeitos ao controle do Tribunal de Contas:</u></strong>
                        </p>

                    <p class="Texto">
                        - Por serem integrantes da estrutura do Estado e executarem atividades com verba pública, se sujeita ao controle do Tribunal de Contas, nos termos do art. 71 da CRFB/88, que poderá julgar contas, aplicar multas e outras sanções.
